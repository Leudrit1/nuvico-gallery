const dictionaries: Record<Lang, Dict> = { de, en };

export function t(key: string): string {
  const dict = dictionaries[currentLang] || dictionaries.de;
  return dict[key] || key;
}

export function getLang(): Lang { return currentLang; }
export function setLang(lang: Lang) {
  if (currentLang !== lang) {
    currentLang = lang;
    localStorage.setItem('lang', lang);
    listeners.forEach((l) => l());
  }
}

export function subscribe(cb: Listener) { 
  listeners.add(cb); 
  return () => { 
    listeners.delete(cb); 
  }; 
}

// Custom hook for language changes
export function useLanguageChange() {
  const [, setRerender] = useState(0);
  
  useEffect(() => {
    const unsub = subscribe(() => setRerender((n) => n + 1));
    return unsub;
  }, []);
}

export function initLang() {
  const saved = (localStorage.getItem('lang') as Lang) || 'de';
  currentLang = saved;
}


